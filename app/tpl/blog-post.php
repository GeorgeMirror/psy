										<h5 class="text-regular">Individual treatment is often termed as psychotherapy, and is meant to help people with their emotional issues, which can range in order of their severity or intensity. The main aim of this form of therapy is to change the quality of life by defining the path of life clearly. Whether it is the problem of repressed childhood that you are facing, or an emotional breakdown a professional psychologist can help you.</h5>
										<p>After you have completed an initial intake appointment and completed the necessary paperwork, your intake interviewer will contact you regarding treatment options. These may include receiving therapy in the selected clinic, or providing you with outside community referrals better equipped to meet your needs.</p><img src="images/gallery-original-02.jpg" width="1200" height="800" alt="">
										<p>Typically, during the initial appointment, you will have a discussion about yourself and the reason(s) you have decided to seek psychotherapy services at this time. Your therapist may also give you additional questionnaires to complete depending upon the difficulties you reported in the intake interview.</p>
